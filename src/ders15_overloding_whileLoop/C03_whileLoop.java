package ders15_overloding_whileLoop;

public class C03_whileLoop {

    public static void main(String[] args) {

        // 2basamakli sayilari aralarinda bir bosluk olacak sekilde yanyan yazdirin.

        for (int i = 10; i<99; i++){
            System.out.println(i +" ");
        }

        // while loop ile yaparsak;

        int a=10;
        while (a<100){

            System.out.print(a+ " ");
            a++;
        }
    }
}
